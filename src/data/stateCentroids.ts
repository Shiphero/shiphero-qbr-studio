export function getLatLngFromZip(zip: string): { lat: number; lng: number; state: string } | null {
  const zip3 = zip.substring(0, 3);
  const result = ZIP3_CENTROIDS[zip3];
  if (result) return result;

  // Try with leading zero if 5-digit zip starts without it
  if (zip.length >= 3) {
    const padded = zip.padStart(5, '0');
    const zip3Padded = padded.substring(0, 3);
    return ZIP3_CENTROIDS[zip3Padded] || null;
  }
  return null;
}
