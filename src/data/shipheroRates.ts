/**
 * ShipHero MRC (Merchant Rate Card) — USPS rates
 * Source: ShipHero USPS Rate Card.xlsx
 *
 * Each row: [maxWeightLbs, z1, z2, z3, z4, z5, z6, z7, z8]
 * For Ground Advantage, weights < 1 lb are stored as fractional lbs (oz / 16).
 */

export type RateRow = [number, number, number, number, number, number, number, number, number];

/** Look up rate for a given weight (lbs) and zone (1-8). Returns null if not found. */
export function lookupMrcRate(table: RateRow[], weightLbs: number, zone: number): number | null {
  if (zone < 1 || zone > 8) return null;
  for (const row of table) {
    if (weightLbs <= row[0]) {
      return row[zone] ?? null;
    }
  }
  // Exceeds max weight — use last row
  const last = table[table.length - 1];
  return last ? last[zone] ?? null : null;
}

