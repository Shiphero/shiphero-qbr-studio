/** Infer the best-matching service key from a ShipHero shippingMethod string. */
export function inferServiceKey(shippingMethod: string): ServiceKey | null {
  const s = shippingMethod.toLowerCase();
  if (s.includes('express')) return 'PME';
  if (s.includes('priority')) return 'PM';
  if (s.includes('ground') || s.includes('ga') || s.includes('advantage')) return 'GA';
  return null;
}
