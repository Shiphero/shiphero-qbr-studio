/**
 * Look up a UPS rate.
 * For Ground: zone is 2-8 (map from USPS zone using: upsZone = uspsZone, since USPS zone 2-8 ≈ UPS zone 2-8)
 * For NDA: zone is 102-108
 * For 2nd Day: zone is 202-208
 * For 3DS: zone is 302-308
 */
export function lookupUPSRate(service: UPSServiceKey, weightLbs: number, zone: number): number | null {
  const table = UPS_RATES[service];
  if (!table) return null;

  const zones =
    service === 'GROUND_COMM' || service === 'GROUND_RES' ? UPS_GROUND_ZONES
    : service === 'NDA_COMM' ? UPS_NDA_ZONES
    : service === 'DAY2_COMM' ? UPS_DAY2_ZONES
    : UPS_DS3_ZONES;

  const zoneIdx = zones.indexOf(zone);
  if (zoneIdx === -1) return null;

  const colIdx = zoneIdx + 1; // +1 because index 0 is weight

  // Find the right weight row (ceiling weight)
  const w = Math.ceil(weightLbs);
  if (w < 1) return null;

  // Find last row with weight <= w (or -1 for 151+)
  let matchRow: [number, ...number[]] | null = null;

  for (const row of table) {
    if (row[0] === -1) {
      // 151+ per-lb rate
      if (w > 150) {
        const overageRow = row;
        // Find rate for exactly 150, add per-lb overage
        const base150 = lookupUPSRate(service, 150, zone);
        if (base150 === null) return null;
        return parseFloat((base150 + (w - 150) * overageRow[colIdx]).toFixed(2));
      }
      continue;
    }
    if (row[0] >= w) {
      matchRow = row;
      break;
    }
  }

  if (!matchRow) return null;
  return matchRow[colIdx] ?? null;
}
